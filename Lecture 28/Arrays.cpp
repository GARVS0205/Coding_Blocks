#include<bits/stdc++.h>
using namespace std;
const int N = 0;


void Solve(char *p) {


	cout << p << endl;
	cout << *(p + 1) << endl;
}

int main() {
	int a[] = {1, 2, 3, 4};

	cout << a << endl;

	cout << a + 1 << endl;
	cout << a + 2 << endl;

	cout << *(a + 1) << endl;

	Solve(a);
}